package _1;

public class Operaciones {

	public static int sumar(int a, int b) {
		return a + b;
	}

	public static int multiplicar(int a, int b) {
		return a * b;
	}

	public int dividir(int a, int b) {
		return a / b;
	}

	public int resta(int a, int b) {
		return a - b;
	}

	public int modulo(int a, int b) {
		return a % b;
	}

}
